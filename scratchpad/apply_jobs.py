tagged = 0
skipped = 0
report = []

for rel_path, rel_link, job in entries:
    fp = base + rel_path
    with open(fp, "r", encoding="utf-8") as f:
        content = f.read()
    lines = content.split("\n")
    out = []
    inserted = False
    already = False
    for i, line in enumerate(lines):
        out.append(line)
        if re.match(r"^\s*-\s*\*\*전투 클래스\*\*", line):
            # check next line for existing 기반 직업
            if i+1 < len(lines) and "기반 직업" in lines[i+1]:
                already = True
                continue
            newline = f"- **기반 직업**: {job} ([직업-매핑-정본]({rel_link}))"
            out.append(newline)
            inserted = True
    if already:
        skipped += 1
        report.append((fp, "skipped-already"))
        continue
    if not inserted:
        skipped += 1
        report.append((fp, "NOT FOUND field"))
        continue
    with open(fp, "w", encoding="utf-8") as f:
        f.write("\n".join(out))
    tagged += 1
    report.append((fp, "tagged"))

print("tagged:", tagged, "skipped:", skipped)
for r in report:
    print(r)
